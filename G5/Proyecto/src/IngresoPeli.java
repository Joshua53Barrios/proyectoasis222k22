    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        conectar cc=new conectar();
        Connection cn=cc.conexion();
        try{
            PreparedStatement pst=cn.prepareStatement("INSERT INTO peliculas(id_peli,nombrePeli,clasificacion,director,fecha_estreno,duracion) VALUES(?,?,?,?,?,?)");
            pst.setString(1,id.getText());
            pst.setString(2,nom.getText());
            pst.setString(3,cla.getText());
            pst.setString(4,direc.getText());
            pst.setString(5,fecha.getText());
            pst.setString(6,dura.getText());

            int a=pst.executeUpdate();
            if(a>0){
                JOptionPane.showMessageDialog(null,"Registro exitoso");
                mostrardatos("");
                setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null,"Error al agregar");
            }
        }catch(Exception e){
    }//GEN-LAST:event_GrabarActionPerformed
    }

